class Solution {
  public:
    int pairsum(vector<int> &arr) {
        int a = arr[0], b = INT_MIN;
        for(int i = 1; i<arr.size(); i++){
            int c = arr[i];
            if(c >= a){
                b = a;
                a = c;
            }else if(c > b){
                b = c;
            }
        }
        return a + b;
    }
};


